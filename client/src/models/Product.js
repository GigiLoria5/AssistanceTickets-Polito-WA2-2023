export function Product(productId, asin, brand, category, manufacturerNumber, name, price, weight) {
    this.productId = productId;
    this.asin = asin;
    this.brand = brand;
    this.category = category;
    this.manufacturerNumber = manufacturerNumber;
    this.name = name;
    this.price = price;
    this.weight = weight;
}
